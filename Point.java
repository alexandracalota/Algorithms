package points;

/**
 * 
 * @author Alexa
 *
 *         Class which defines a point. Each point has a value.
 * 
 */
public class Point {
	public int value;

	/**
	 * Constructor which initializes the value attribute of a Point instance.
	 * 
	 * @param value
	 */
	public Point(int value) {
		this.value = value;
	}
}
